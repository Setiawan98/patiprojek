package projekpati.com.projekpati.Model.BeritaOnline;

public class GambarBeritaOnlineDetil {

    String file_beritaOnline_id, file_beritaOnline_img;

    public GambarBeritaOnlineDetil(String file_beritaOnline_id, String file_beritaOnline_img) {
        this.file_beritaOnline_id = file_beritaOnline_id;
        this.file_beritaOnline_img = file_beritaOnline_img;
    }

    public String getFile_beritaOnline_id() {
        return file_beritaOnline_id;
    }

    public void setFile_beritaOnline_id(String file_beritaOnline_id) {
        this.file_beritaOnline_id = file_beritaOnline_id;
    }

    public String getFile_beritaOnline_img() {
        return file_beritaOnline_img;
    }

    public void setFile_beritaOnline_img(String file_beritaOnline_img) {
        this.file_beritaOnline_img = file_beritaOnline_img;
    }
}
