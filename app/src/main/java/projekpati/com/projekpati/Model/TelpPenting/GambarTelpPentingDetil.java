package projekpati.com.projekpati.Model.TelpPenting;

public class GambarTelpPentingDetil {
    String file_telp_penting_id, file_telp_penting_img;

    public GambarTelpPentingDetil(String file_telp_penting_id, String file_telp_penting_img) {
        this.file_telp_penting_id = file_telp_penting_id;
        this.file_telp_penting_img = file_telp_penting_img;
    }

    public String getFile_telp_penting_id() {
        return file_telp_penting_id;
    }

    public void setFile_telp_penting_id(String file_telp_penting_id) {
        this.file_telp_penting_id = file_telp_penting_id;
    }

    public String getFile_telp_penting_img() {
        return file_telp_penting_img;
    }

    public void setFile_telp_penting_img(String file_telp_penting_img) {
        this.file_telp_penting_img = file_telp_penting_img;
    }
}
